package Basics;

public class switchCase {
    public static void main(String args[]) {
        int number = 2;
        switch (number) {
            case 1:
                System.out.println("samosa");
                break;
            case 2:
                System.out.println("cake");
                break;
            default:
                System.out.println("wake up");

        }
    }
}
